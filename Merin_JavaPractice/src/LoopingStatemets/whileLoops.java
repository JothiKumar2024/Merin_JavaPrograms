package LoopingStatemets;

public class whileLoops {

	public static void main(String[] args) {
		
		int a;
		a=1;
		while(a<=5)
		{
			System.out.println("a value is "+a);
			a++;
		}
		System.out.println("------------------------------------");
		a=5;
		while(a>=1)
		{
			System.out.println("a value is "+a);
			a--;
		}
		System.out.println("------------------------------------");
		a=2;
		while(a<=20)
		{
			System.out.println("a value is "+a);
			a+=2;
		}
		
		
		
	}

}
